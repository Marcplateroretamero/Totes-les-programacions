using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Ex_6__13_Octubre_Mostrar_nums_del_10_al_0
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Mostar los números del 10 al 0.
            for (int i = 10; i >= 0; i--)
            {
                Console.WriteLine("Numero : " + i);
            }
        }
    }
}
